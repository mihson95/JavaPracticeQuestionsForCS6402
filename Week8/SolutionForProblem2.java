package Week8;

import java.util.*;

public class SolutionForProblem2 {


    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        System.out.println("Get First Number as User Input");
        int a = sc.nextInt();
        System.out.println("Get Second Number as User Input");
        int b = sc.nextInt();
        int sum = a + b;

        System.out.println("Sum of 2 numbers = " + sum);
        sc.close();
    }
    
}
